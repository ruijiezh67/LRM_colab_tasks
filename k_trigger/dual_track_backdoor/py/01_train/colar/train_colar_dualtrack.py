r"""
train_colar_dualtrack.py
========================
Continue-train the FROZEN-LATENT colar-gsm checkpoint into a DUAL-TRACK model that emits

    [question ###(<bot>)]  [FROZEN latent chain]  ###(<eot>)  <cot> visible-CoT </cot>  \boxed{answer}

with a BOTTLENECK attention mask: the answer-generating query rows may attend ONLY to
(question + latent + <eot>), NEVER to the visible-CoT keys <cot>..</cot>.

Freeze-latent design (why this does NOT collapse the latent like Latent-SFT did)
--------------------------------------------------------------------------------
colar-gsm's latent is already causal (token-swap ~0.95).  We DO NOT retrain it.
* The latent embeds are PRE-generated by bootstrap_colar_latents.py from the frozen
  colar-gsm and spliced in here as fixed, DETACHED inputs (no gradient, no latent loss).
* latent_policy is loaded and FROZEN (and never called during training).
* The answer reads those original causal latents through the bottleneck mask, so the
  latent stays causal BY CONSTRUCTION — the only thing training changes is the
  (question+latent+<eot>) -> {visible-CoT, answer} generation.

Token mapping for CoLaR (no <think>/</think> tokens exist in CoLaR):
    <bot>  = the trailing "###" already inside the CoLaR prompt (part of the P prefix)
    <eot>  = the "###" sep_id token appended after the latent chain (as in causal_test_colar.gen)
    <cot>/</cot> = the ONLY new tokens added here (resize + init from the "###" sep embedding)

Trainable (everything else frozen):
    * the existing colar q/v LoRA adapter, at a LOW lr (--lr)   -> the CoT/answer generator
    * the two new <cot>/</cot> embedding rows (gradient-masked on the tied embedding weight)
Loss = cot_w * CE(labels_cot) + ans_w * CE(labels_answer).  NO latent / KL loss.

Loader + LatentPolicy + LoraConfig + emb_std + sep_id are copied VERBATIM from
causal_test_colar.py so loading matches the checkpoint exactly.  The 4-D bottleneck
mask geometry + _shifted_ce + <cot>/</cot> handling are copied from the Latent-SFT
template (patched_data.py / patched_modeling_stage2.py).

Env (Colab /content vs local E:/ckpts):
    COLAR_BASE COLAR_CKPT COLAR_EMB_STD(0.018) COLAR_COMPRESS(5)
Run:
    python train_colar_dualtrack.py --selftest              # pure-python: assembly + mask geometry
    COLAR_BASE=... COLAR_CKPT=.../colar_best.ckpt \
      python train_colar_dualtrack.py --data <clean.jsonl> --latents <dir> --output_dir <dir>
"""
